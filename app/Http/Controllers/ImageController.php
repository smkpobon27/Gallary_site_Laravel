    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        if ($request->hasFile('image')) {
            $imageName = $request->image->store('public/images');
        }
        $newImage = new Image;
        $newImage->title = $request->title;
        $newImage->description = $request->description;
        $newImage->image_url = $imageName;
        $newImage->category_id = $request->category;
        $newImage->privacy = $request->privacy;
        $newImage->user_id = Auth::user()->id;
        $newImage->save();

        return redirect('/');
    }

